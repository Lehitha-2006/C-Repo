#include<stdio.h>
int main()
{
	int n;
	printf("enter the number\n",n);
	scanf("%d",&n);
	
	if(n==1)
	{
		printf("sunday\n");
	}
	else if(n==2)
	{
		printf("monday\n");
	}
	else if(n==3)
	{
		printf("tuesday\n");
	}
	else if(n==4)
	{
		printf("wednesday\n");
	}
	else if(n==5)
	{
		printf("thursday\n");
	}
	else if(n==6)
	{
		printf("friday\n");
	}
	else if(n==7)
	{
		printf("saturday\n");
	}
	else
	{
		printf("enter a valid number between 1 to 7");
	}
	return 0;
}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	

